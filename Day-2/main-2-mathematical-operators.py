# mathematical operators
n1 = 10
n2 = 20

# addition
addition = n1 + n2
print(f"addition = {addition}")

# multiplication
multiplication = n1 * n2
print(f"multiplication = {multiplication}")

# subtraction
subtraction = n1 - n2
print(f"subtraction = {subtraction}")

#  true division => float (decimal)
division = n1 / n2
print(f"division = {division}, data type = {type(division)}")

# floor division => int (whole)
division = n1 // n2
print(f"division = {division}, data type = {type(division)}")

# 10 to the power of 4
power = 10 ** 4
print(f"power = {power}")

# remainder (modulus)
mod = 11 % 2
print(f"mod = {mod}")

# string * operator
# repeats the string "-*-" 20 times
print("-*-" * 20)

# string + operator => concatination
print("hello" + "world")
